# 使用字典来存储一个人的信息
person_info = {
    'first_name': 'John',
    'last_name': 'Doe',
    'age': 30,
    'city': 'New York',
}

# 将存储在该字典中的每项信息都打印出来
print(f"First name: {person_info['first_name']}")
print(f"Last name: {person_info['last_name']}")
print(f"Age: {person_info['age']}")
print(f"City: {person_info['city']}")
